package com.mmall.model;

public class RechargePackage {
    private Integer id;

    private Long rechargeMonry;

    private Long rechargeObject;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Long getRechargeMonry() {
        return rechargeMonry;
    }

    public void setRechargeMonry(Long rechargeMonry) {
        this.rechargeMonry = rechargeMonry;
    }

    public Long getRechargeObject() {
        return rechargeObject;
    }

    public void setRechargeObject(Long rechargeObject) {
        this.rechargeObject = rechargeObject;
    }
}